function value =  get_data_from_varargin(measure_code, numel, dimension, rule, varargin)
    if Measure.is_global(measure_code)
        value = get_from_varargin( ...
            repmat( ...
            {0}, ...
            1, ...
            numel), ...
            rule, ...
            varargin{:});
    elseif Measure.is_nodal(measure_code)
        value = get_from_varargin( ...
            repmat( ...
            {zeros(dimension, 1)}, ...
            1, ...
            numel), ...
            rule, ...
            varargin{:});
    elseif Measure.is_binodal(measure_code)
        value = get_from_varargin( ...rep
            repmat( ...
            {zeros(dimensions)}, ...
            1, ...
            numel), ...
            rule, ...
            varargin{:});
    end
end